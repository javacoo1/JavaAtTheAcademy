
public class Calculator {
	//	필드(속성)
	//	표현할 속성이 없음.
	//	필드를 정의할 필요 없음.
	
	//	객체를 만들 필요도 없음
	
	//	static 메서드 (클래스 메서드)
	public static void add(int a, int b) {
		System.out.println(a + b);
	}
	
	//	일반 메서드 (인스턴스 메서드) <- 인스턴스를 생성해야만 사용할 수 있으니께
	public void addThree(int a, int b, int c) {
		System.out.println(a + b + c);
	}
	
	
	
	
	
	
	
}
